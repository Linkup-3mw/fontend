import { suit } from '@styles/typography';
import '@styles/globals.css';
import { Header } from '@common/header';
import AuthContext from '@/context/AuthContext';
import AuthorizationHeader from '@/context/AuthorizationHeader';
import type { Metadata } from 'next';
import RecoilProvider from '@/context/Provider';
import QueryProvider from '@/context/QueryProvider';
import { fetchServerSession } from '@/utils/session';

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await fetchServerSession();

  return (
    <html lang="ko">
      <body className={`${suit.className} bg-blue-100`}>
        <QueryProvider>
          <RecoilProvider>
            <AuthContext>
              <AuthorizationHeader>
                <Header initialSession={session} />
                <div>{children}</div>
              </AuthorizationHeader>
            </AuthContext>
          </RecoilProvider>
        </QueryProvider>
      </body>
    </html>
  );
}
